class OneShape:
    def __init__(self, n1: int, c1: str):
        self.number_1 = n1
        self.color_1 = c1


class TwoShapes:
    def __init__(self, n1: int, n2: int, c1: str, c2: str):
        self.number_1 = n1
        self.number_2 = n2
        self.color_1 = c1
        self.color_2 = c2


class ThreeShapes:
    def __init__(self, n1: int, n2: int, n3: int, c1: str, c2: str, c3: str):
        self.number_1 = n1
        self.number_2 = n2
        self.number_3 = n3
        self.color_1 = c1
        self.color_2 = c2
        self.color_3 = c3
